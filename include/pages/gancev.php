                <p>Stojan Stojanov Gančev tenor (Ajtos) Bugarska 1929. Studij solo pjevanja završio je u klasi
poznatog bugarskog vokalnog pedagoga Hriste Brmbarova u Sofiji. Nakon završetka studija solo
pjevanja 1960., počeo je studirati I opernu režiju kod Dragana Karadžieva. Još u vrijeme studija,
1957. debitirao je u sofijskom Državnom muzikalnom teatru kao Caramello u opereti Jedna noć u
Veneciji Johanna Straussa te je odmah bio angažiran u mnogim drugim projektima, uglavnom u
operetama. Iste godine je dobio zlatnu medalju na Omladinskom festivalu u Moskvi.
Već dvije godine nakon, 1959. osvojio je prvu nagradu na natjecanju “Ferenc Erkel” u Budimpešti.
1960. do 1963. je bio član sofijske Opere. 1961. se predstavio i u Varni kao Rodolpho u operi La
Boheme te je iste godine osvojio prvu nagradu za herojski repertoar u Sofiji. Napredujući u sofijskoj
operi, redatelji su mu povjeravali sve teži i zahtjevniji program. 1964. Stojanov je odlučio otići u
Makedoniju te je tamo ostao sve do 1969. Kao solist Opere u Skoplju. 1969. dolazi u Zagreb gdje
je kao solist u Hrvatskom narodnom kazalištu djelovao punih 25 godina te je u Hrvatskoj ostao sve
do svoje smrti.
U svojoj karijeri je otpjevao više od 82 uloge, od toga 34 u Zagrebu. Nakon dolaska u Zagreb
predstavio se ulogom Porina iz istoimene opere Vatroslava Lisinskog. U to vrijeme, HNK je vapio
za tenorom te je Stojanov popunio onaj fah koji od vremena Josipa Gostiča nije bio pokriven.
Ubrzo su mu dodijeljene mnogo zahtjevnije uloge kao Ramades (G. Verdi; Aida), Enzo (A.
Ponchielli; Giaconda). Ugledan i strog glazbeni kritičar Nenad Turkalj uspjeh predstava je pripisao
odličnim protagonistima, Branki Stilinović i Stojanu Stojanovu Gančevu te ga je istaknuo kao
umjetnika izrazitog formata zahvaljujući njegovim glasovnim kvalitetama, sigurnim visinama te
izražajnom igrom dok je Jagoda Martinčević napomenula kako je Stojanov svojom glumom,
izgledom i glasom napokon Ramades kojem se može vjeroavati. Sljedeći veliki uspjeh je uslijedio s
ulogom Miće (Ere) iz Gotovčeve opere Ero s onoga svijeta. Koliko je bio uvjerljiv, naglasio je i sam
Gotovac rekavši kako je rođen za tu ulogu koju je izveo u većini opernih kuća tadašnje Jugoslavije
te na mnogo međunarodnih gostovanja. Svladavao je izrazito različite uloge, davao je veliku
pozornost psihološkoj karakterizaciji lika te na raspolaganju imao bogat i raznovrsan repertoar. Prvi
vrhunac njegovo djelovanja u HNK-u, Stojanov je postigao ulogom Manrica, iz Verdijevog Il
trovatore. Kao dramski tenor koji je pjevao cabalettu u C-duru, bio je potpuna senzacija. Zajedno
na sceni izvodio je s pjevačicama i pjevačima svjetskih glasova kao što su Ljiljana Molnar Talajić,
Biserka Cvejić, Ferdinand Radovan i Franjo Petrušanec. U svibnju 1976. otpjevao je ulogu po kojoj
biva danas najzapamćeniji; Verdijevog Otella. Sve njegove kvalitete su tom ulogom došle do punog
izrađaja; muzikalnost, visoka glazbena i opća kultura, interpretativno umijeće.
Nenad Turkalj nije štedio riječi hvale i priznanja; “Ulogom Otella tenor Stojan Stojanov Gančev
ostvario je vrhunac svog dosadašnjeg djelovanja na zagrebačkoj opernoj sceni. Glasovno svjež i
dovoljno majestozan, skoncentriran na glazbeno-misaonu frazu teksta, bio je izgledom i glumom
pravi Verdijev Otello.”; “Otello Stojana Stojanova njegova je do danas najzrelija uloga na
Zagrebačkoj pozornici. Glasovno joj je dorastao, a interpretativno je u nju uronio jednako svojom
sposobnošću da emotivno artikulira tekst, kao i da se scenom kreće i glumi na način krajnje
saživljen opernom pjevanju, ali uvijek dovoljno decentan.”. Uz blistavog Otella, cijela postava pod
vodstvom dirigenta Nikše Bareze je toliko mamila uzdaha da je snimka uvrštena na
međukontinentalni program Euroclassic Notturno. Uloga Otella 1977.donijela je Stojanovu
prestižnu nagradu “Milka Trnina”. Izuzetnom brzinom nastavio je nizati mnoge nove uloge. U
vrijeme kad je bio stalan član Zagrebačke opere, bio je stalni gost Beogradske ,Sarajevske i
Skopske opere. U tim opernim kućama je ostvario brojne druge uloge, a i mnogo gostovanja van
Jugoslavije gdje su ga slušali ljubitelji operne umjetnosti u SSSR-u, ČSSR-u, Poljskoj, Rumunjskoj,
SR Njemačkoj, Švicarskoj, Francuskoj, Finskoj, Italiji, Španjolskoj, Grčkoj, Egiptu, a i u rodnoj
Bugarskoj.</br></br>

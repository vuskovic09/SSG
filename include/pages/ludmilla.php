                <p>Ludmilla Aloysia Weiser rođena je 09.kolovoza 1847. u Zagrebu.Otac joj je bio uvaženi zagrebački majstor glazbalar Ivan Nepomuk Weiser, jedan od prvih izučenih glazbalara u Zagrebu. Zbog kvalitetnih tambura Franjo Ks. Kuhač nazvao ga je „Amatijem” tambure, a važno je napomenuti da je bio jedan od najranijih zagrebačkih glazbalara koji je održavao, izrađivao i popravljao glazbala HGZ-a.</br>
Ludmilla Weiser upisala je glazbenu školu u HGZ-u 1856.godine kod učitelja Antuna Schwarza.
Već je na učeničkim produkcijama  Ludmilla pokazala iznimnu vještinu zbog koje je poslije postala  širokoj javnosti poznata glazbena umjetnica.O zapaženom talentu svjedoče i skladbe koje je Antun Schwarz napisao za Ludmillu. </br>
Prvi zapažen solistički nastup Ludmilla je imala u Zagrebu 21.lipnja 1862.kao gošća na koncertu poznate operne pjevačice Liduine Capelli. </br>
Godine 1862. Ravnateljstvo HGZ-a odobrilo joj je stipendiju od 120 forinti zbog lošeg imovinskog stanja roditelja i poslalo je na Bečki konzervatorij kod poznatog pedagoga i učitelja violine Josepha Hellmesbergera starijeg.Kraljevsko namjesničko vijeće joj je, na preporuku HGZ-a1863. Odobrilo potporu na još dvije godine. </br>
Ludmilla Weiser bila je prva studentica violine kao glavnog predmeta u navedenoj glasovitoj bečkoj obrazovnoj ustanovi. </br>
Za potvrdu njezine važnosti u onodobnom glazbenom svijetu najvažniji su brojni koncerti održani od 1860 do 1868.a pronađene su i kritike koncerata koje potvrđuju iznimnu vrijednost ”prve hrvatske umjetnice na guslah”. Iz tih se kritika jasno vidi koliko se teško bilo probiti u muškom svijetu glazbe unatoč neospornom talentu. </br>
Dana 06.prosinca 1868.Ludmilla Weiser održala je vjerojatno posljednji službeni koncert u Zagrebu u Narodnom kazalištu za potrebe prikupljanja sredstava za izgradnju nove zgrade Hrvatskog pjevačkog društva”Kolo”.</br>
Ludmilla Weiser vjenčala se 02.svibnja 1869.u bečkoj luteransko-evangeličkoj crkvi za znamenitog estonskog arhitekta Reinholda Ludwiga Ernsta Gulekea i završila s aktivnom glazbenom karijerom.
Luteranska obitelj Reinholda Gulekea teško je prihvatila školovanu i priznatu violinsku virtuoskinju rimokatoličke vjere te je u predbračnom ugovoru inzistirala da Ludmilla prestane javno nastupati.
Umrla je u Jeni 25.prosinca 1933. i pokopana je na starom gradskom groblju Nordfriedhof uz supruga Reinholda, potpuno zaboravljena u glazbenoj povijesti. </br></br>


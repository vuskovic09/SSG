                <p>Rođena je u Gruziji, u obitelji poznatih glazbenika, među kojima je bio i V. Kandelaki, poznati
sovjetski pjevač, glumac, filmski redatelj, nacionalni umjetnik SSSR-a. Njezin otac, Vladimir
Kandelaki, je pjevač, solist Opere, profesor, nacionalni umjetnik Gruzije, a majka je Medea
Tsirgiladze, pijanistica i profesor.
1975. godine maturirala je u specijalnoj glazbenoj školi za posebno darovitu djecu Z. Paliashvili pri
konzervatoriju &#39;Saradjišvili&#39; u Tbilisiju, gdje je učila u klasi, počasnog umjetnika, profesora M.
Chavchanidzea.</br>
Upisala je Moskovski državni konzervatorij P. I. Čajkovski u klasi profesora L. Naumova.
1981. godine završila je studij, te je primljena na poslijediplomski doktorski studij u klasu T.
Nikolaeve, izvanredne pijanistice, profesora, laureata državne nagrade, nacionalne umjetnice
SSSR-a. Doktorirala je u siječnju 1984. godine.
Sa solističkim koncertima, a takoder u ansamblima s instrumentalistima i pjevačima nastupila je u
gotovo svim velikim gradovima SSSR-a.
Od 1981. solistica je Gruzijske filharmonije i Goskoncerta SSSR-a.
Od 1987. godine na preporuku Tatjane Petrovne Nikolaeve postala je profesor na Centralnoj
glazbenoj školi za posebno darovitu djecu (CMŠ), pri Moskovskom državnom konzervatoriju P. I.
Čajkovski.</br></br>
Profesor je Medunarodne škole pedagoškog i izvodačkog umijeća (Ukrajina, Hrvatska)
Tijekom rada u njezini su učenici i studenti vise od 70 puta postali laureati međunarodnih
natjecanja.
Za uspješnu pripremu svojih studenata za koncertne turneje i međunarodna natjecanja, dobila je
niz priznanja za visoka pedagoška dostignuća - u Moskvi, Parizu, Kopenhagenu, Italiji, Ukrajini,
Španjolskoj.
Njezini studenti Vyacheslav Gryaznov, Elizaveta Klyuchereva, Nail Mavliudov, i drugi imaju
koncertne turneje solistički i sa orkestrom po cijelom svijetu u dvoranama: Berlinska filharmonija,
Velika dvorana moskovskog konzervatorija , Carnegie Hall...
Neki od njih rade na moskovskom konzervatoriju, solisti su filharmonije, a takoder i u SAD-u,
Izraelu,Japanu, Hrvatskoj, Gruziji.
Manana Kandelaki je savjetnik u ruskim dobrotvornim fondovima V. Spivakova, V. Kraineva, i
fondu «Nova imena».
Sudjeluje u radu mnogih medunarodnih žirija: Italija, Danska, Srbija, Gruzija, Rusija, Ukrajina.</br></br>

Održavala je seminare te masterclass u mnogim gradovima bivseg SSSR - a,Rusiji i inozemstvu.
12 godina provodila je masterclass tečajeve u gradovima Hrvatske: Zagrebu, Grožnjanu, Puli,
Rijeci, za što je nagrađena diplomom &#39;za doprinos razvoju hrvatske glazbene umjetnosti&#39;. Mnogo
je puta sudjelovala u televizijskim i radijskim emisijama, njezin masterclass uvršten je u
dokumentarni film &#39;Grožnjan - Grad umjetnosti&#39;</br>

Međunarodno udruženje «International Teachers Association» dodijelilo joj je 1998. priznanje
«Izvanredan učitelj». a 2009. godine nagrađena je Zahvalnicom ministra kulture Ruske Federacije
A. Avdejeva «Za veliki doprinos u razvoju kulture i dugogodišnji plodonosan rad».</br></br>

2011. godine udruga «Prima Artist International Competition Committee» dodijelila joj je certifikat
«Za izvanredno umijeće» («Certifikat izvrsnosti»).
Od 2015 Manana Vladimirovna Kandelaki ima klavirsku klasu na državnom konzervatoriju
Saradjishvili u Tbilisiju, a takoder predaje i na specijalnoj srednjoj školi pri konzervatoriju.
Aktivno i sa velikim uspjehom radi on line,sa mnogim mladim glazbenicima u svijetu.
Njezini studenti nastavljaju sa uspjehom nastupati diljem svijeta.</p>

                <p>LJUBICA ŽIVKOVIĆ rođena je u Beogradu,1952. godine. Poslije završene srednje
glazbene škole "Josip Slavenski", odjel za glazbenu teoriju i solo pjevanje, studije je
nastavila na FMU u Beogradu, na oba odjela: teoriju u klasi prof. Petra Ozgijana, a
solo pjevanje u klasi prof. Zvonimira Krnetića. Diplomirala je u klasi Radmile
Smiljanić.
 U toku studija aktivno je nastupala na mnogim europskim koncertnim scenama,
kao solistica zbora "Collegium Musicum". Za muzičku omladinu, u okviru ciklusa
"Mladi za mlade", ostvarila je više od stotinu koncerata. Kao apsolvent je položila
audiciju u Beogradskoj operi, te trinaest sezona, u 360 predstava nastupa kao
solistica (Traviata, Carmen, Seviljski brijač, Boris Godunov, Konzul, Kći puka, Pikova
dama, Prodana nevesta...). U Ljubljani je gostovala tri godine u "Borisu Godunovu", a
u Novom Sadu kao solistica u operama Traviata, Carmen i Seviljski brijač. U filmu
"Boris Godunov" sa Rugierom Raimondijem igrala je ulogu Njanje. Usporedno sa
scenskom djelatnošću, tokom 23 godine bila je solistica Umjetničkog ansambla na
koncertima u zemlji i inozemstvu. Od 1994. bavi se i pedagoškim radom, najprije u
glazbenoj školi "Jovan Bandur" u Pančevu, a onda prelazi u najstariju beogradsku
glazbenu školu "Stanković". Takoder, u isto vrijeme, putuje u Banja Luku i pomaže u
razvoju solo pjevačkog odjela u glazbenoj školi "Vlado Milošević", a od 2004. ima i
svoju klasu na Akademiji lijepih umjetnosti u Beogradu.
 Sa svojim učenicima i studentima sudjeluje na natjecanjima solo pjevanja u zemlji i
inozemstvu: "Jeunesse Musical", Međunarodno takmičenje mladih muzičara "Petar
Konjović", Republička takmičenja Srbije i Republike Srpske, Međunarodno
takmičenje "Lazar Jovanović" , "Dani solo pesme" u Lazarevcu, Takmičenje pevača
"J. Marinković" u Beogradu, "Obzorja na Tisi" u Novom Bečeju, Festival muzičkih
škola Srbije, "Nikola Cvejić" u Rumi.
 Njeni učenici i studenti osvojili su više od dvjesto nagrada. Poslije završene
srednje škole, 28 učenika iz njene klase nastavilo je školovanje na FMU i Akademiji
lijepih umjetnosti u Beogradu, kao i na Muzičkim akademijama u Novom Sadu, Banja
Luci, Grazu, Mannheimu . Kao solisti u Operi i ansamblu Narodnog pozorišta, Opere
i teatra Madlenianum i Pozorišta na terazijama nastupaju Nenad Nenić, Dragoljub
Bajić, Nebojša Babić, Iva Mrvoš, Dragana Stanković, Tamara Banješević, Igor
Arizanović, Vladimir Bulatović, Mirjana Jovanović, Pavle Žarkov.
 Osnovala je ansambl "Operetika", sastavljen od sadašnjih i bivših učenika i
studenata. Na repertoaru su četiri različita programa: dječja opera "Snježana i sedam
patuljaka" Petra Krstića, operete i mjuzikli, operne arije i ansambli, kao i srpsko
nacionalno blago. Uz stalnu suradnju sa Muzičkom omladinom Srbije, sa ovim
programima nastupala je u mnogim gradovima Srbije (Pančevo, Zrenjanin, Valjevo,
Obrenovac, Sopot, Mladenovac, Kovin, Beograd). Ansambl je nastupao i u
Kolarčevoj zadužbini, Sava centru, pavilijonu "Cvijeta Zuzorić", Skupština grada, kao
i u "Banskom dvoru" u Banja Luci, pozorištima u Temišvaru, Budimpešti i Moskvi.
 Učestvovala je u radu žirija na takmičenjima: "Petar Konjović" u Beogradu, "Nikola
Cvejić" u Rumi, Republičko takmičenje u Srbiji i Republici Srpskoj, "Dani solo pesme" 
u Lazarevcu, Međunarodnom takmičenju "Davorin Jenko", Festivalu muzičkih škola
Srbije, takmičenje solo pevača "J. Marinković" i "Raspevano proleće" u Pančevu.
Dobitnik je nagrade Udruženja muzičkih i baletskih pedagoga Srbija za postignute
rezultate u dugogodišnjem pedagoškom radu. </p>

                <p>She was born in Georgia, in a family of famous musicians, among whom was V. Kandelaki, a
famous Soviet singer, actor, film director, national artist of the USSR. Her father, Vladimir
Kandelaki, is a singer, opera soloist, professor, national artist of Georgia, and her mother is Medea
Tsirgiladze, a pianist and professor.
In 1975, she graduated from the Z. Paliashvili Special Music School for Particularly Gifted
Children at the Saradjishvili Conservatory in Tbilisi, where she studied in the class of Honorary
Artist, Professor M. Chavchanidze.
She enrolled in the Moscow State Conservatory P. I. Tchaikovsky in the class of Professor L.
Naumov.</br></br>
In 1981, she completed her studies and was admitted to postgraduate doctoral studies in the
class of T. Nikolaeva, associate pianist, professor, laureate of the state award, national artist of the
USSR. She received her doctorate in January 1984.
She performed at solo concerts, and also in ensembles with instrumentalists and singers in almost
all major cities of the USSR.
Since 1981, she has been a soloist of the Georgian Philharmonic and the Gosconcert USSR.
Since1987, upon the recommendation of Tatiana Petrovna Nikolaeva, she became a professor at
the Central Music School for Particularly Gifted Children (CMŠ), at the P.I.Tchaikovsky Moscow
State Conservatory.
She is a professor at the International School of Pedagogical and Performing Arts (Ukraine,
Croatia)</br></br>
In the course of her work at this School, its pupils and students became laureates of international
competitions more than 70 times.
She received a number of awards for high pedagogical achievements - in Moscow, Paris,
Copenhagen, Italy, Ukraine, Spain, for the successful preparation of her students for concert tours
and international competitions,
Her students Vyacheslav Gryaznov, Elizaveta Klyuchereva, Nail Mavliudov, and others have
concert tours solo and with orchestra around the world in the most renowned concert halls: the
Berlin Philharmonic, the Great Hall of the Moscow Conservatory, Carnegie Hall ...
Some of them work at the Moscow Conservatory, are soloists of the Philharmonic, and also in the
USA, Israel, Japan, Croatia, Georgia.
Manana Kandelaki is an advisor in the Russian charitable funds of V. Spivakov, V. Krainev, and
the fund &quot;New Names&quot;.
She participates in the work of many international juries: Italy, Denmark, Serbia, Georgia, Russia,
Ukraine.</br></br>

She has held seminars and master classes in many cities of the former USSR, Russia and
abroad.
For 12 years she conducted masterclass courses in Croatian cities: Zagreb, Grožnjan, Pula,
Rijeka, for which she was awarded a diploma &#39;for her contribution to the development of Croatian
musical art&#39;. She has participated in television and radio shows many times, her masterclass was
included in the documentary &#39;Grožnjan - City of Art&#39;
In 1998, the International Teachers Association awarded her the &quot;Outstanding Teacher&quot; award,
and in 2009 she was awarded the Certificate of Appreciation of the Minister of Culture of the

Russian Federation A. Avdeyev &quot;For great contribution to the development of culture and long-term
fruitful work.&quot;</br></br>

In 2011, the Prima Artist International Competition Committee awarded her the Certificate of
Excellence (Certificate of Excellence).
Since 2015, Manana Vladimirovna Kandelaki has had a piano class at the Saradjishvili State
Conservatory in Tbilisi, and also teaches at a special high school at the conservatory.
She works actively and with great success online, with many young musicians in the world.
Her students continue to perform successfully around the world.</p>

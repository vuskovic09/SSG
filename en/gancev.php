                <p>Stojan Stojanov Gančev was a tenor born in Ajtos, Bulgaria in 1929. In 1960 he completed his
studies in solo singing in the class of the famous Bulgarian vocal pedagogue Hristo Brmbarov in
Sofia. Afterward, he studied opera direction in the class of Dragan Karadžiev. While still a student,
he made his debut in 1957 as Caramello in Johan Strauss’ operetta A Night in Venice performed in
Sofia State Musical Theater. Soon after he was signed for many other projects, mainly operettas.
The same year he was awarded a gold medal at the Youth Festival in Moscow.
In 1959 he won the first prize in Ferenc Erkel Competition in Budapest. From 1960 to 1963 he was
a member of the Sofia Opera. In 1961 he performed in Varna as Rodolpho in La Boheme and that
same year he won the first prize for the heroic repertoire in Sofia.
His success in the Sofia Opera was recognized by the directors allowing him to perform in a more
demanding program. In 1964 Stojan decided to transfer to Macedonia where he remained until
1969. As a Skopje Opera soloist in 1969, he came to the Croatian National Theater in Zagreb
where he performed for the next 25 years, until his death.
In his career, he performed in more than 82 roles, of which 34 in Zagreb.
His Zagreb debut was in the role of Porin in Vatroslav Lisinski’s opera Porin. At the time, the
Croatian National Theater was in demand of a tenor, and Stojan filled the gap which remained after
Josip Gostič. Soon more demanding roles followed, such as Ramades (G. Verdi; Aida), Enzo (A.
Ponchielli; Giaconda). Nenad Turkalj, a reputable and strict music critic, attributed the success of
the play to excellent protagonists, Branka Stilinović and Stojan Stojanov Gančev, and pointed
Stojan out as an artist of distinctive format thanks to his vocal qualities, safe pitches, and
expressive performance. Another critic, Jagoda Martinčević noted that Stojanov’s acting,
appearance, and voice finally make for a convincing Ramades.
His next great success followed with the role of Mićo (Ero) from the Jakov Gotovac opera Ero the
Joker. Jakov Gotovac himself emphasized how convincing he was as if born for the role, that he
performed in most of the opera houses in former Yugoslavia and abroad. He mastered different
roles and paid special attention to the psychological profile of the characters. A rich and varied
repertoire contributed even more to his characterization.
The height of his activity in the Croatian National Theater was achieved with the role of Manrico,
from Verdi's Il trovatore. As a dramatic tenor who sang cabaletta in C major, he was a complete
sensation. He performed on stage together with world-famous singers such as Ljiljana Molnar
Talajić, Biserka Cvejić, Ferdinand Radovan, and Franjo Petrušanec.
In May 1976, he sang the role for which he is best remembered today; Verdi's Othello. All his
qualities came to full expression with this role; musicality, high musical, and general culture,
interpretive art. Nenad Turkalj did not spare words of praise and recognition; "With the role of
Othello, tenor Stojan Stojanov Gančev achieved the culmination of his work so far on the Zagreb
opera scene. His voice fresh and majestic enough, focused on the music and thought the lyrics, he
was a true Verdi’s Otello in appearance and acting. ”; "Otello performed by Stojan Stojanov is his
most mature role on the Zagreb stage to date. He is up to the role vocally, and interpretively
immersed himself in the role equally with his ability to emotionally articulate the lyrics, as well as
moving and acting on the stage in a way extremely sympathetic to opera singing, but always
decent enough. ”. Along with the dazzling Othello, the entire ensemble under the direction of
conductor Nikša Bareza was so enticing that the recording was included in the intercontinental
program Euroclassic Notturno. The role of Othello in 1977 brought Stojan the prestigious "Milka
Trnina" award. He continued to play many new roles at an extraordinary speed. At the time when
he was a permanent member of the Zagreb Opera, he was a regular guest of the Belgrade,
Sarajevo, and Skopje Opera. He performed many other roles in these opera houses, as well as
many guest appearances outside Yugoslavia where he was listened to by opera lovers in the
USSR, Czechoslovakia, Poland, Romania, FR Germany, Switzerland, France, Finland, Italy, Spain,
Greece, Egypt, and in his native Bulgaria.</br></br>

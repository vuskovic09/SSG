                <p>Ana Veskov was born and raised in Belgrade. She finished Primary music school “Mokranjac” in the class of prof. Vlajko Savković, and she graduated from Musical High School “Stanković” in Belgrade, in the class of prof. Olga Bešević. Having successfully passed three entrance examinations for full-time studies in Belgrade, Sofia and Russia, she decided to continue further education in Leningrad, modern St. Petersburg, city which was celebrated by some of the most famous global musicians, writers and painters… Education received on Russian state conservatory formed her future personality, character, style and professionalism regarding the music, interpretation and pedagogy. 
</br>
During the 6-year study, she received education from classes of renowned pedagogues such as Aleksander Fisher, Marko Mogilevskiy and Aleksander Stang, finishing the basic studies and master’s degree with the calling of a soloist, member of a chamber music ensemble, string quartet, orchestra and a violin pedagogue. She participated on the national and international competitions, winning, as a soloist, more than 10 first prizes. During her time in Russia, she performed a number of noticed concerts in the St. Petersburg’s Philharmonic, Academy Chapel “Glinka” and Gala Hall “N. R. Korsakov” Conservatory, performing as a soloist, member of the orchestra and string quartet “Tchaikovsky”. She attended master classes of Yehudi Menuhin, Itzhak Perlman, Pinchas Zukerman, Gidon Kremer, Zakhar Bron, amongst many others. 
</br>
During her studies she accepted all the importance of pedagogical work in which into which she stepped in through globally renowned violin pedagogue, Saveliy Shalman, in whose classes she studied methodology and pedagogical practice, teaching and actively working with children throughout her studies. 

After her return to Belgrade, she continues her professional development at the Faculty of Music in Belgrade, in the class of prof. Olivera Đurdjević, and next to perennial performing activity as a soloist, chamber musician and member of Belgrade Philharmonic, she starts, in parallel, with professorial work in Elementary School “Stanković”. Soon she moved, with whole of her class, to Musical School “J. Š. Slavenski”, where she continued to work as elementary and high school professor. 
</br>
She continues to work there to this day, with 29 year of internship. Students from her class won over 140 first classes at the national, republic and international competitions – 35 of which are absolute. Most significant international competitions are “Kocian” (Czech Republic), “Postacchini” (Italy), “Nedjalka Simeunova” (Bulgaria), “Giuseppe Tartini” (Slovenia), “Grimio” (Belgium), “Ohrid Pearls” (Macedonia), Franjo Krežma (Croatia), whilst national are Republican Competition and competitions in Niš, Sremska Mitrovica, Sremski Karlovci, amongst many others. For the last 15 years she actively holds master classes and she is often a part of the jury on the national and international level competitions. She’s the president of Belgrade and Republic section of the violinist in the frame Community of musical and ballet schools of Serbia, from which she, after 25 years of successful work, received an Award of Life’s Work, in 2017. She is fluent in six languages and, in parallel with musical career, has an extensive 20-year long activity in journalism and organization.</br>

Having an affinity towards pedagogic work with young children, with whom she strives to achieve high results even during the first years of their schooling, she decided to define and direct her innovative approach on a special way, by founding Master Art Academy in 2014., center for training, concerts and further professional guidance and orientation for young, promising, talented musicians. With help of her colleagues from the around the globe, she realized numerous additional training courses via workshops and master classes, having exchange of information and experiences with professors and their students as a primary goal. In short time since it’s founding, Master Art Academy has realized more than 200 concerts, 50 master classes and three Summer Academies. Catering the need for special gala concerts, 2015. she founded the “Master Art Academic” orchestra, comprised of young talents who are still having active concert careers, and are performing in numerous musical projects. 


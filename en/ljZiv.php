                <p>LJUBICA ZIVKOVIC was born in Belgrade in 1952. After having finished Secondary Music School
"Josip Slavenski", departments of music theory and solo singing, she continued her studies at the
Faculty of Music in Belgrade, at both departments: music theory with Prof. Petar Ozgijan and solo
singing with Prof. Zvonimir Krnetić. She graduated from the class of Prof. Radmila Smiljanić.
During her studies, she performed actively as a soloist with the "Collegium Musicum" choir at
numerous European concert stages. She performed at more than a hundred concerts for the Music
Youth within the "Youth for Youth" cycle. As a final year student, she passed the audition for the
National Opera of Belgrade, where she was performing as a soloist for 13 seasons in 360 operas
(La Traviata, Carmen, The Barber of Seville, Boris Godunov, The Consul, The Daughter of the
Regiment, The Queen of Spades, The Bartered Bride, etc. ). She was a guest singer for the opera
"Boris Godunov" in Ljubljana for three years. She performed as a soloist for La Traviata, Carmen,
and the Barber of Seville in Novi Sad. She played the role of Nanny in the film "Boris Godunov"
with Ruggiero Raimondi. Alongside her stage activity, she was a soloist of the Art Ensemble at
concerts in the country and abroad for 23 years. She started her pedagogical career in 1994, first
at Music School "Jovan Bandur" in Pančevo, then at the oldest Belgrade Music School "Stanković".
During three years, she was traveling to Banja Luka to help the development of the solo singing
department of Music School "Vlado Milošević". She formed her class at the Academy of Fine Arts
in Belgrade in 2004. With her pupils and students, she has participated in solo singing competitions
both in the country and abroad: "Jeunesse Musical", the International Competition of Young
Musicians "Petar Konjovic", Republic competitions of Serbia and the Republic of Srpska,
International Singing Competition "Lazar Jovanović", "The Days Solo Singing" competition in
Lazarevac, Singers Competition "J. Marinković" in Belgrade, "The Dawn on the Tisa" in Novi Bečej,
the Festival of Music and Ballet Schools of Serbia, "Nikola Cvejić" competition in Ruma. Her pupils
and students have won more than two hundred prizes. After having finished secondary music
school, 28 of her pupils continued their education at the Faculty of Music and the Academy of Fine
Arts in Belgrade and the Music Academies of Novi Sad, Banja Luka, Graz Mannheim. She has
founded the vocal ensemble "Operetika", which consists of her current and former pupils and
students.
There have been four different programs on their repertoire: Petar Krstić's opera for children "Snow
White and the Seven Dwarfs", operettas and musicals, opera arias and ensembles, and the
Serbian national treasure. With the constant cooperation with the Music Youth Serbia, they
performed these programs in many towns (Pančevo, Zrenjanin, Valjevo, Obrenovac, Sopot,
Mladenovac, Kovin, and Beograd). The ensemble also performed at the Ilija M. Kolarac
Foundation, Sava Centre, "Cvijeta Zuzorić" Pavilion, Belgrade City Assembly, and "Halls of the
Ban" cultural center in Banja Luka and theatres in Timisoara, Budapest, and Moscow. She has
participated as a jury member at the following competitions: "Petar Konjović" in Belgrade, "Nikola
Cvejić" in Ruma, Republic competitions of Serbia and the Republic of Srpska, "The Days of Solo
Singing" in Lazarevac, "Davorin Jenko" International Competition, the Festival of Music and
Ballet Schools of Serbia, Singers Competition "J. Marinkovic and "Raspevano proleće "in Pančevo.
She was awarded a prize from the Association of Music and Ballet Pedagogues of Serbia for the
results achieved in her long-time pedagogical work.</p>

                <p>ARIJANA GIGLIANI PHILIPP was born in Sarajevo (1979), where she graduated
secondary school of violin and solo singing. Following graduation at Academy of
Music in Zagreb, she continued to study voice with renowned pedagogue and tenor
principal Stojan Stojanov Gančev from Bulgaria.
As a soloist she has performed with ensemble Zagreb Soloists, Sarajevo
Philharmonic Orchestra, Osijek Theater Orchestra, Dubrovnik Symphony Orchestra,
Croatian Radio Symphony Orchestra and others. She enrolled in several recording
sessions for the Fono-archive of Croatian Radio.
She made her professional debut in the leading role of Jelena in the opera Nikola
Šubić-Zrinski by Ivan pl. Zajc at the Sarajevo National Theater and with the same
role, she made guest performances with glowing reviews in Bosnia and
Herzegovina. In the National Theater Ivan pl. Zajc, in Rijeka she was invited to
sing the role of Lady-In-Waiting in Macbeth and the role of Berta in The Barber of
Seville.
In the Theater in Osijek, she was widely recognized for her performance of Jelena
in Nikola Šubić-Zrinski.She was giving guest performances in the role of Adalgisa
in Norma and in the role of Abigaille in Nabucco, as well as providing open air
concerts with National Croatian Theater in Osijek.
She performed as a soloist with conductors Vjekoslav Šutej, Nikša Bareza, Julije
Marić, Nada Matošević, Dario Vučić, Filip Pavišić, Mladen Tutavac, Ognjen Bomoštar,
Tomislav Fačini and others. During the past years, she regularly gives recitals in
Croatia and abroad with pianists Ivan Batoš, Barbara Bego, Ana Anconelli and Mario
Čopor.
In 2008 she won the first prize and was laureat of the overall international
competition Lazar Jovanović in Belgrade (Serbia). In 2010 she won the first prize at
the International Competition Bruna Špiler Solo singers in Herceg Novi
(Montenegro).
Arijana Gigliani Philipp is actively involved in classical, opera and contemporary
music, cooperating closely with Croatian composers whose works she regularly
performs.
For years her chamber music activity is permanently associated with the trio
Solenza. In 2016 label 'Croatia Records' published their second compact disc
entitled «Mosaic of Croatian Composers» which was nominated for the national disc
prize 'Porin'.
As a pedagogue her profound knowledge and experience in concert performing
made her reputation of highly skilled mentor of singing. Throughout the years,
together with her pupils, she was awarded many national and international prizes
in singing as well as in the chamber music.
She works as a professor of opera singing at Music Conservatory Zlatko Baloković
in Zagreb.</p>

                Croatian pianist and pedagogue Nenad Kačar was born in 1965 in Zagreb.
He has been involved with music since his early childhood, and as a five-year-old, he
had his first public performance. During his schooling through he sensitized the public
through numerous concerts, and gets noticed which, in return, made it possible to to
obtain prestigious national scholarships.
He studied and graduated at the Zagreb Academy of Music in the class of a prominent
Croatian pianist and professor, academician Jurica Muraja. He continued his training
with numerous pianists, E. Timakin, M. Lorković A. Preger, R. Kerer, M. Farre and I.
Žukov.</br></br>
He has performed in almost all major music centers of Croatia as well as in numerous
European countries (Slovenia, Italy, Germany, the Czech Republic, the Netherlands,
Belgium, Sweden, Bulgaria, Macedonia and Russia, etc.). He recorded for various
domestic and foreign radio and TV stations. In the field of chamber music, he has
collaborated with a number of prominent musicians, conductors, ensembles and
orchestras.</br>
Since 2017 he has collaborated in piano duo with pianist Nataša Mitrović.
In March 2019, at the Croatian Music Institute in Zagreb, he performed with the Croatian
Chamber Orchestra and conductor Diana Tchobanov, also performing, with great
success, the solo part of the Mozart Concert K.V.466 which will be released on CD.</br></br>
Since 1987, he has been engaged in pedagogical activities and, with results, he
presents the range of Croatian piano pedagogy. Regularly, his students are winners of
numerous competitions, winning first prizes at domestic and international competitions
(more than 200), performing at a number of well-publicized performances and concerts
(over 540) and participating in media events in a dozen European countries (Russia,
USA, Italy , Germany, Austria, France, the Netherlands Macedonia, Bosnia and
Herzegovina, Serbia, Slovenia and Croatia).</br></br>
Particular emphasis should be given to the performances of his students at the world
famous halls at Carnegie Hall in New York, the Rachmaninov Concert Hall of the
Moscow State Conservatoire, the Vienna Music Store, the Berlin Philharmonic Hall, the
Auditorium Parco della Musica, the Teatro Studio in Rome, the Concertgebouw in
Amsterdam, the Casa di Riposo G. Verdi in Milan, the church of St. Sofia in Ohrid, a
church. St. Donata in Zadar, KD Vatroslav Lisinski in Zagreb and others.</br></br>

He is a longtime chief of the piano department of the Academy of Music Zlatko
Baloković. He is a mentor to numerous young pianist pedagogues until they are
prepared for the professional state exam.
He has held a series of professional seminars, masterclasses and lectures in Croatia,
Europe and Russia. He was invited as a member of the jury for many international piano
concerts.</br></br>
He publishes professional works in the field of piano pedagogy and piano methodology.
He is a member of EPTA Croatia and the HDGPP Association. He has been a guest
lecturer to the students of the 4 th and 5 th year of the Music Academy in Zagreb, within
the framework of Pedagogical Practice Crouse, since the school year of 2009/10.
Based on the results achieved, the Ministry of Science, Education and Sports and
AZOO nominate him and promoted him into a mentor-professor in 2011 and in 2016 as
professor advisor.
His artistic pedagogical work was rewarded with numerous awards

                <p>Natasha Mitrovic, pianist, is a native of Belgrade.
She received her undergraduate and graduate degrees with
excellent results at the Music Academy in Belgrade where she now
teaches piano.
Further studies included studies in Vienna with prof. Avo
Kouyoumdjian and piano studies with prof. Jan Novotny in
Prague.</br>
Natasha Mitrovic was the only artist from Serbia to be awarded
Fulbright scholarship in year 2004. During her Fulbright grant, Ms.
Mitrovic studied at the University of Michigan (Ann Arbor), at
Julliard University (New York) and at de Paul University
(Chicago) where she performed extensively with great acclaim.
The resent one was in the famous Kennedy Centre in Washington
(DC).
During her Fulbright grant, she worked on the project ‘’Music
without borders’’ which is about similarities and differences
between Serbian and American pianistic schools.
Ms.Mitrovic has performed 15 times as a piano soloist with all
major orchestras in Serbia and also with the Bulgarian
Philharmonic and the Simphony orchestra of Frankfurt and
Montabaur (Germany).</br></br>

Ms. Mitrovic has won many prizes for piano performances in
Serbia and abroad:</br>
- prize of the Music Academy in Belgrade for the best student in
generation and the prize for the best graduate exam at the piano
department,</br>
- prize “Emil Hajek” for the best pianist at the Music Academy,
- Octobar prize of the city of Belgrade for the highest
achievements in music,</br>
- prize of the Music Academy in Belgrade for the best performance
of Beethoven sonata,</br>
- acknowledgement from the Music Academy in Belgrade for her
contribution as pedagogue and performer.</br></br>

She also won many prizes at piano competitions and festivals in
former Yugoslavia as a soloist and in chamber music.
She has given around 50 recitals and performances in France, Italy,
the Czech Republic, Macedonia, Bulgaria, Germany, Sweden,
United States, China, Turkey, Kuwait. She is very often a member
of the jury in international piano competitions and she often runs
master classes for piano students in Europe.
She was one of the representatives of Belgrade Music Academy on
the Festival “Nei Suoni Dei Luonghi” in 2010. in Italy, where she

had a recital. She also had a concert which was a part of Festival of
Modern music in Skoplje (Macedonia) in 2012. In July 2013. she
was the President of the jury on International piano competition
“Jozef Suk” in Prague (Czech Republic), where she also played in
famous Martinu Hall.
As a member of Music Society “Poetica Musica” every year she
plays in Hall “Old Westbury Gardens” in New York (USA).
She often plays as a member of various chamber ensembles.
Since 2017. she actively plays in piano duo with Croatian pianist
Nenad Kačar.</br>
She has recorded for television and radio in Belgrade, Novi Sad,
Macedonia and with RAI, in Italy.
She is a member of Fulbright board in Belgrade. As a
representative of Serbia on Fulbright Congress in Beijing (China)
in 2008. she gave a lecture. With collaboration of Serbian Embassy
in Beijing, she performed solo recital in Serbian Embassy in
Beijing and also in Tokyo, Japan (in 2014).</br></br>

She has been working in the piano department as an associate
professor on the Belgrade Faculty of Music since 1998.
She held a number of classes and seminars for students and young
artists in the USA, Spain, Turkey, Israel, Greece, Bulgaria,
Macedonia, Serbia etc. She’s a regular jury member in numerous

international and domestic competitions. Among her pedagogic
accomplishments, her students actively play and win prizes on
domestic and international competitions.                </p>

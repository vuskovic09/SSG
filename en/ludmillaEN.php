                <p>Ludmilla Aloysia Weiser was born 9th of August 1847. in Zagreb. Her father was renowned master luthier Ivan Nepomuk Weiser, one of the first professional luthiers in Zagreb. Because of his quality tamburas Franjo Ks. Kuhač called him „Amatti of tambura”, and is important to notice that he was one of the earliest in Zagreb who created, maintained and repaired the instruments of HGZ. In 1856. Ludmilla started attending classes of Antun Schwarz at the Musical school of HGZ. She displayed exceptional skill and talent already at the pupil productions, which made her known to the wider audience as musical artist. Compositions of Antun Schwarz which were dedicated to Ludmilla bear testament to her immense talent. </br>
                First noticed soloist performance Ludmilla had in Zagreb, on 21st of June 1862. as a guest performer at the concert of renowned opera singer Liduine Capelli. </br>
                1862. directorate of HGZ granted her stipend of 120 forints because of poor financial conditions of her parents, and sent her to the Viennese Conservatory, where she attended classes of the famous pedagogue and violin teacher Joseph Hellmesberger Sr. Governor's council extended financial support for two more years, on recommendation of HGZ. </br>
                Ludmilla Weiser was first female student of violin at the forementioned prestigious Viennese educational institution. 
                </br>
                As a confirmation of her artistic importance in those times, she performed at numerous concerts from 1860. to 1868., and there were critiques of the concerts, which confirmed exceptional value of „first Croatian artist on fiddle”. Those critiques confirm how hard it was to break through in the men's world, in spite of indisputable talent. On 6th of December 1868. Ludmilla Weiser held last official concert in Zagreb, in National theater, as a fundraiser for the construction of new building of National singing association „Kolo”. Ludmila Weiser was married to renowned Estonian architect Reinhold Ludwig Ernst Guleke, on 2nd of May 1869. in luteran-evangelical church, in Vienna, and she finished her active musical career.
                </br>
                Guleke's conservative luteran family hardly accepted educated and famous virtuoso violinist of Roman Catholic faith, and demanded, in the premarital contract, that she stops performing publicly. She died in Jena, 25th of December 1933., and buried on old city cemetery Nordfriedhof along with her husband Reinhold, forgotten in the musical history. 
